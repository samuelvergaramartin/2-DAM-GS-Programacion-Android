package com.example.ejemploadaptadorimagenes;

public class Datos {

    private int icono;
    private String texto;

    public Datos(int icono, String texto) {
        this.icono = icono;
        this.texto = texto;
    }

    public int getIcono() {
        return icono;
    }

    public String getTexto() {
        return texto;
    }
}
