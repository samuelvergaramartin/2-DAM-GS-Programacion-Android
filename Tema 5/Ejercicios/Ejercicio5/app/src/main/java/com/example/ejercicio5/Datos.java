package com.example.ejercicio5;

public class Datos {
    private String nombre;
    private String telefono;

    public Datos(String nombre, String telefono) {
        this.nombre = nombre;
        this.telefono = telefono;
    }

    public String getNombre() {
        return nombre;
    }

    public String getTelefono() {
        return telefono;
    }
}
